package com.mobatia.vkcsalesapp.model;

public class DealersModel {
	private String mName;
	private String mCountry;
	private String mState;
	private String mDistrict;
	private String mCity;
	private String mPostCode;
	private String mStreet;
	private String mTelephone;
	public String getName() {
		return mName;
	}
	public void setName(String mName) {
		this.mName = mName;
	}
	public String getCountry() {
		return mCountry;
	}
	public void setCountry(String mCountry) {
		this.mCountry = mCountry;
	}
	public String getState() {
		return mState;
	}
	public void setState(String mState) {
		this.mState = mState;
	}
	public String getDistrict() {
		return mDistrict;
	}
	public void setDistrict(String mDistrict) {
		this.mDistrict = mDistrict;
	}
	public String getCity() {
		return mCity;
	}
	public void setCity(String mCity) {
		this.mCity = mCity;
	}
	public String getPostCode() {
		return mPostCode;
	}
	public void setPostCode(String mPostCode) {
		this.mPostCode = mPostCode;
	}
	public String getStreet() {
		return mStreet;
	}
	public void setStreet(String mStreet) {
		this.mStreet = mStreet;
	}
	public String getTelephone() {
		return mTelephone;
	}
	public void setTelephone(String mTelephone) {
		this.mTelephone = mTelephone;
	}

}
