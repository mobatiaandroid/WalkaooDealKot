package com.mobatia.vkcsalesapp.model;

public class ReportDetailModel {
	
	private String gift_name;
	private String gift_qty;
	private String rwd_points;
	private String tot_coupons;
	public String getGift_name() {
		return gift_name;
	}
	public void setGift_name(String gift_name) {
		this.gift_name = gift_name;
	}
	public String getGift_qty() {
		return gift_qty;
	}
	public void setGift_qty(String gift_qty) {
		this.gift_qty = gift_qty;
	}
	public String getRwd_points() {
		return rwd_points;
	}
	public void setRwd_points(String rwd_points) {
		this.rwd_points = rwd_points;
	}
	public String getTot_coupons() {
		return tot_coupons;
	}
	public void setTot_coupons(String tot_coupons) {
		this.tot_coupons = tot_coupons;
	}
	
	

}
